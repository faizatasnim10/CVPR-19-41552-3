.MODEL SMALL
.STACK 100H

.DATA

MSG1 DB "ENTER A CHARECTER: $"
MSG2 DB "IT IS A CAPITAL LETTER$"
MSG3 DB "IT IS A SMALL LETTER$" 
MSG4 DB "ENTER A NUMBER: $"
MSG5 DB "IT IS A POSITIVE NUMBER$"
MSG6 DB "IT IS ANEGATIVE NUMBER$"
MSG7 DB "IT IS EQUAL TO ZERO$"

.CODE

MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1 ;MSG1
INT 21H

MOV AH,1 ;IN CHR
INT 21H
MOV BH,AL 

MOV AH,2
MOV DL,0AH ;NL
INT 21H
MOV DL,0DH
INT 21H       

CMP BH,"A";CMP
JB CAPITAL
JA SMALL      

CAPITAL:
MOV AH,9
LEA DX,MSG2 ;MSG2
INT 21H 

JMP NUMBER

MOV AH,2
MOV DL,0AH ;NL
INT 21H
MOV DL,0DH
INT 21H

SMALL:
MOV AH,9
LEA DX,MSG3 ;MSG3
INT 21H

JMP NUMBER

NUMBER:  
MOV AH,2
MOV DL,0AH ;NL
INT 21H
MOV DL,0DH
INT 21H

MOV AH,9
LEA DX,MSG4 ;MSG4
INT 21H

MOV AH,1 ;IN NUM
INT 21H
MOV BH,AL 

CMP BH,"0";CMP
JG POSITIVE
JL NEGATIVE
JZ ZERO

POSITIVE: 
MOV AH,2
MOV DL,0AH ;NL
INT 21H
MOV DL,0DH
INT 21H

MOV AH,9
LEA DX,MSG5 ;MSG5
INT 21H 

JMP END

NEGATIVE: 
MOV AH,1 
INT 21H

MOV AH,2
MOV DL,0AH ;NL
INT 21H
MOV DL,0DH
INT 21H

MOV AH,9
LEA DX,MSG6 ;MSG6
INT 21H

JMP END

ZERO: 
MOV AH,2
MOV DL,0AH ;NL
INT 21H
MOV DL,0DH
INT 21H

MOV AH,9
LEA DX,MSG7 ;MSG7
INT 21H
          

END:
MOV AH,4CH ;END
INT 21H