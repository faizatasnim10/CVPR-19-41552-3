.model small
.stack 100h  

.data   

msg db "POSITIVE NUMBER $"
msg1 db "NEGATIVE NUMBER $" 

.CODE

MOV AX,@DATA
MOV DS,AX

MOV AH,2
MOV BH,-4
CMP BH,0   

JGE POSITIVE
JL NEGATIVE

POSITIVE:
MOV AH,9
LEA DX,MSG
INT 21H
JMP EXIT 

NEGATIVE:
MOV AH,9
LEA DX,MSG1
INT 21H 

EXIT:
MOV AH,4CH
INT 21H
